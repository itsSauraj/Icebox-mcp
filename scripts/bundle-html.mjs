/**
 * Inlines each app's built single-file HTML into `generated/html.js` so the
 * server needs no runtime filesystem access (works in Node, Vercel, Workers).
 * Runs after `build:apps` (which produces dist/*.html).
 */
import fs from "node:fs/promises";
import path from "node:path";

const FILES = [
  "color-picker.html",
  "dice.html",
  "coin.html",
  "card.html",
  "wheel.html",
  "decision-dice.html",
  "wordle.html",
  "snake.html",
];

const root = process.cwd();
const distDir = path.join(root, "dist");
const outDir = path.join(root, "generated");

const map = {};
for (const file of FILES) {
  map[file] = await fs.readFile(path.join(distDir, file), "utf-8");
}

const body =
  `// AUTO-GENERATED by scripts/bundle-html.mjs — do not edit.\n` +
  `// Regenerate with \`npm run bundle:html\` (part of \`npm run build\`).\n` +
  `export const htmlByFile = ${JSON.stringify(map)};\n`;

await fs.mkdir(outDir, { recursive: true });
await fs.writeFile(path.join(outDir, "html.js"), body, "utf-8");

console.log(`Wrote generated/html.js (${FILES.length} apps, ${(body.length / 1024).toFixed(0)} KB)`);
